public class BallTask2 {

    private int num;
    private int ball;

    public BallTask2(int num, int ball) {
        super();
        this.num = num;
        this.ball = ball;
    }

    public BallTask2() {
        super();
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public int getBall() {
        return ball;
    }

    public void setBall(int ball) {
        this.ball = ball;
    }

}
